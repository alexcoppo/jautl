package net.sf.jautl.numeric.geometry.impl;

import net.sf.jautl.collections.arrays.DoubleTuple;

/**
 *
 */
public class DoublePair implements DoubleTuple {
    protected double x;
    protected double y;
    
    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public int getDimensions() {
        return 2;
    }

    @Override
    public double get(int index) {
        switch (index) {
            case 0: return getX();
            case 1: return getY();
            default: return 0;
        }
    }

    @Override
    public void set(int index, double value) {
        switch (index) {
            case 0: setX(value); break;
            case 1: setY(value); break;
        }
    }
}
